#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t c;

	i = 0;
	c = 0;
	while (s1[i])
	{
		i++;
	}
	while (c < n)
	{
		s1[i + c] = s2[c];
		c++;
	}
	s1[i + c] = '\0';
	return (s1);
}
